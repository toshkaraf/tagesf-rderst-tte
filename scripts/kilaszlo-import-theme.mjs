/**
 * Converts kilaszlo lib/models/theme_data.dart → src/ki/theme/themeData.gen.ts
 * Usage: node scripts/kilaszlo-import-theme.mjs [path/to/theme_data.dart]
 * Default: kilaszlo/ inside this repo if present, else ../kilaszlo/ (sibling).
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.join(__dirname, '..')
/** Monorepo: kilaszlo/ inside this repo. Override with argv or KILASZLO_THEME_DART */
const defaultDart = fs.existsSync(path.join(repoRoot, 'kilaszlo', 'lib', 'models', 'theme_data.dart'))
  ? path.join(repoRoot, 'kilaszlo', 'lib', 'models', 'theme_data.dart')
  : path.resolve(repoRoot, '..', 'kilaszlo', 'lib', 'models', 'theme_data.dart')
const dartPath = path.resolve(process.argv[2] || process.env.KILASZLO_THEME_DART || defaultDart)
const outPath = path.join(repoRoot, 'src', 'ki', 'theme', 'themeData.gen.ts')

function convertTopics(str) {
  return str.replace(
    /Topic\(id: '([^']+)', name: '([^']*)', description: (null|'([^']*)')\)/g,
    (_, id, name, descPart, descStr) => {
      const description = descPart === 'null' ? 'null' : JSON.stringify(descStr)
      return `{ id: ${JSON.stringify(id)}, name: ${JSON.stringify(name)}, description: ${description} }`
    }
  )
}

function main() {
  if (!fs.existsSync(dartPath)) {
    console.error('Missing file:', dartPath)
    console.error('Pass the path to kilaszlo theme_data.dart as the first argument.')
    process.exit(1)
  }
  let dart = fs.readFileSync(dartPath, 'utf8')
  dart = convertTopics(dart)

  const marker = '// Comprehensive theme data'
  const i = dart.indexOf(marker)
  if (i === -1) throw new Error('Marker not found: ' + marker)

  let part1 = dart.slice(0, i).trimEnd()
  const part2 = dart.slice(i + marker.length).trimStart()

  part1 = part1
    .replace(
      /final Map<String, List<Topic>> topicSubtopics = \{/,
      'export const topicSubtopics: Record<string, KiTopic[]> = {'
    )
    .replace(
      /final Map<String, Map<String, String>> _categoryTranslations = \{/,
      'export const categoryTranslationsDe: Record<string, { name: string; description: string }> = {'
    )
    .replace(
      /final Map<String, String> _subcategoryTranslations = \{/,
      'export const subcategoryTranslationsDe: Record<string, string> = {'
    )
    .replace(
      /final Map<String, Map<String, String>> _topicTranslations = \{/,
      'export const topicTranslationsDe: Record<string, { name: string; description?: string }> = {'
    )

  // Remove leading Dart classes / Topic class block (up to topicSubtopics comment)
  const subIdx = part1.indexOf('// Подтемы')
  if (subIdx !== -1) {
    part1 = part1.slice(subIdx)
  }

  let tail = part2
    .replace(/final List<ThemeCategory> themeCategories = \[/, 'export const themeCategories: KiThemeCategory[] = [')
    .replace(/ThemeCategory\(/g, '{')
    .replace(/ThemeSubcategory\(/g, '{')
    .replace(/\r\n/g, '\n')
    .replace(/\n        ],\n      \),/g, '\n        ],\n      },')
    .replace(/\n    ],\n  \),/g, '\n    ],\n  },')

  const header = `/* eslint-disable max-len */
/**
 * AUTO-GENERATED by scripts/kilaszlo-import-theme.mjs from kilaszlo theme_data.dart — do not edit by hand.
 * Re-run: node scripts/kilaszlo-import-theme.mjs
 */

export type KiTopic = { id: string; name: string; description: string | null }
export type KiSubcategory = { id: string; name: string; topics: KiTopic[] }
export type KiThemeCategory = { id: string; name: string; description: string; subcategories: KiSubcategory[] }

`

  const body = `${header}${part1}\n\n${tail}\n`
  fs.mkdirSync(path.dirname(outPath), { recursive: true })
  fs.writeFileSync(outPath, body, 'utf8')
  console.log('Wrote', outPath)
}

main()
